#include <iostream>
using namespace std;
int main(){
   // integer data types
    int age = 22 ;
    cout << age << endl;

   //  character data types
    char name = 'I' ;
    cout << name << endl;

   // float data types
    float percentage = 97.5f;
    cout << percentage << endl;

   // boolean data types
    bool isSafe = true;
   cout << isSafe << endl;
   
   // double data types
    double price = 100.99 ;
   cout << price << endl;

    return 0;
 }